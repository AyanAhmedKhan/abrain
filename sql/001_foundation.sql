-- ── observability: one view answering "what's stuck?" ───────
create or replace view gb_pipeline_status as
select status,
       count(*)                          as items,
       min(ingested_at)                  as oldest,
       max(ingested_at)                  as newest
from gb_envelope
group by status
order by items desc;
